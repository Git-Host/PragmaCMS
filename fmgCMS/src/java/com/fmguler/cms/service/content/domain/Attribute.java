package com.fmguler.cms.service.content.domain;

/**
 * Represents each content fragment in the system.
 * Type - attribute - value triplet.
 * @author Fatih Mehmet GÃ¼ler
 */
public class Attribute {
    public static final String TYPE_RICH_TEXT = "rich-text";
    private Integer id;
    private String type = TYPE_RICH_TEXT;
    private String attribute;
    private String value;

    /**
     * @return the id
     */
    public Integer getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(Integer id) {
        this.id = id;
    }

    /**
     * @return the type
     */
    public String getType() {
        return type;
    }

    /**
     * @param type the type to set
     */
    public void setType(String type) {
        this.type = type;
    }

    /**
     * @return the attribute
     */
    public String getAttribute() {
        return attribute;
    }

    /**
     * @param attribute the attribute to set
     */
    public void setAttribute(String attribute) {
        this.attribute = attribute;
    }

    /**
     * @return the value
     */
    public String getValue() {
        return value;
    }

    /**
     * @param value the value to set
     */
    public void setValue(String value) {
        this.value = value;
    }
}
